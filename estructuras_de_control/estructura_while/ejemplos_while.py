if __name__ == "__main__":

    """
      while condición:
        Código que se ejecuta mientras la condición
        sea verdadera
      else:
        Código que se ejecuta
        cuando la condición del while se evalúa a False

    """
    i = 4
    while i <= 5:
        print(i)
        i+=1
    else:
        print("Se ejecuta el bloque else")








 
    
